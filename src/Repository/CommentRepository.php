    public function getPagination($trick, $start, $limit)
    {
        $qb = $this->createQueryBuilder('c')
            ->select('c')
            ->andWhere('c.trick = '.$trick)
            ->setFirstResult($start)
            ->setMaxResults($limit)
            ->orderBy('c.publishDate', 'DESC');

        $pag = new Paginator($qb);

        return $pag;
    }
